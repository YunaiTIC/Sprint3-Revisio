CREATE TABLE EMPRESA (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(25),
    JOB VARCHAR(25),
    DESCRIPTION VARCHAR(25),
    LOCATION VARCHAR(25)
);

CREATE TABLE OFERTES (
    ID INTEGER PRIMARY KEY AUTO_INCREMENT,
    OFFER VARCHAR(25),
    DESCRIPTION VARCHAR(25),
    EMPRESA_ID INTEGER,
    FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESA(ID)
);
